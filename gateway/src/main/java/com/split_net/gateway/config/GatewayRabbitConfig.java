package com.split_net.gateway.config;

import com.split_net.gateway.infrastructure.db.config.GatewayConfig;
import lombok.RequiredArgsConstructor;
import org.springframework.amqp.core.Binding;
import org.springframework.amqp.core.BindingBuilder;
import org.springframework.amqp.core.FanoutExchange;
import org.springframework.amqp.core.Queue;
import org.springframework.amqp.support.converter.Jackson2JsonMessageConverter;
import org.springframework.amqp.support.converter.MessageConverter;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;


@Configuration
@RequiredArgsConstructor
public class GatewayRabbitConfig {

    private final GatewayConfig gatewayConfig;


    @Bean
    public Queue gatewayQueue() {
        String gatewayId = gatewayConfig.getSubject();
        return new Queue("gateway." + gatewayId + ".events", true);
    }

    @Bean
    public Binding binding(Queue gatewayQueue, FanoutExchange userEventsExchange) {
        return BindingBuilder.bind(gatewayQueue).to(userEventsExchange);
    }

    @Bean
    public MessageConverter jsonMessageConverter() {
        return new Jackson2JsonMessageConverter();
    }
}






























































































































































































































































































